function BejegyzesekGenerator(tomb, betoltes)
{
    if(betoltes)
    {
        for (let i = 0; i < tomb.length; i++) 
        {
            let bejegyzesKartya = document.createElement("div")
            bejegyzesKartya.classList.add("bejegyzes")

            let bejegyzesKep = document.createElement("img")
            bejegyzesKep.src = blogbejegyzesek[i].kepeleres
            bejegyzesKep.classList.add("blogkep")

            let bejegyzesRovidSzovParent = document.createElement("div")
            bejegyzesRovidSzovParent.classList.add("blogszoveg")

            let bejegyzesCim = document.createElement("h3")
            bejegyzesCim.textContent = blogbejegyzesek[i].cim

            let bejegyzesRovidSzov = document.createElement("p")
            bejegyzesRovidSzov.textContent = blogbejegyzesek[i].shortContent

            let bejegyzesTovabb = document.createElement("button")
            bejegyzesTovabb.classList.add("blogOpenBtn")
            bejegyzesTovabb.textContent = "Tovább"
            bejegyzesTovabb.setAttribute('onclick', `OpenPopup(${i})`)

            bejegyzesRovidSzovParent.appendChild(bejegyzesCim)
            bejegyzesRovidSzovParent.appendChild(bejegyzesRovidSzov)
            bejegyzesRovidSzovParent.appendChild(bejegyzesTovabb)

            bejegyzesKartya.appendChild(bejegyzesKep)
            bejegyzesKartya.appendChild(bejegyzesRovidSzovParent)

            document.getElementById("blogresz").appendChild(bejegyzesKartya)

            
        }
    }
}

function OpenPopup(elemSorszam)
{
    let popupMain = document.createElement("div")
    let leftSide = document.createElement("div")
    let rightSide = document.createElement("div")
    let popupBg = document.createElement("div")
    let popupContent = document.createElement("div")
    let popupClose = document.createElement("button")

    leftSide.classList.add("popupBaloldal")
    let kepLeft = document.createElement("img") 
    kepLeft.classList.add("popupKepLeft")
    kepLeft.src = blogbejegyzesek[elemSorszam].kepeleres

    let termekLeft = document.createElement("h3")
    termekLeft.classList.add("popupLeftTermekCim")
    termekLeft.textContent = blogbejegyzesek[elemSorszam].cim

    let arLeft = document.createElement("h3")
    arLeft.classList.add("popupLeftAr")
    arLeft.textContent = `$${blogbejegyzesek[elemSorszam].ar}`

    let atcLeft = document.createElement("button")
    atcLeft.textContent = "Megtekintés"
    atcLeft.classList.add("atcGombLeft")

    rightSide.classList.add("popupJobboldal")
    let cimJobb = document.createElement("h2")
    cimJobb.textContent = blogbejegyzesek[elemSorszam].longContent.bejegyzCim
    cimJobb.classList.add("popupCimRight")

    let szovegJobb = document.createElement("div")
    szovegJobb.textContent = blogbejegyzesek[elemSorszam].longContent.tempSzov
    szovegJobb.classList.add("popupSzovegRight")

    popupClose.onclick = function()
    {
        popupMain.remove();
    }
    popupClose.textContent = "X"

    popupBg.onclick = function()
    {
        popupMain.remove();
    }
    
    popupBg.classList.add("popupBgOpaque")
    popupContent.classList.add("popupCont")
    popupClose.classList.add("popupCloseBtn")

    popupBg.textContent = " "
    popupMain.appendChild(popupBg)
    popupMain.appendChild(popupContent)
    popupBg.appendChild(popupClose)
    document.body.appendChild(popupMain)
    popupContent.appendChild(leftSide)
    leftSide.appendChild(kepLeft)
    leftSide.appendChild(termekLeft)
    leftSide.appendChild(arLeft)
    leftSide.appendChild(atcLeft)
    rightSide.appendChild(cimJobb)
    rightSide.appendChild(szovegJobb)
    popupContent.appendChild(rightSide)
}



